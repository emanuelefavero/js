// unshift(), adds new items to the START of an array, and returns the new length.
const fruits = ['Banana', 'Apple', 'Mango']

console.log(fruits.unshift('Kiwi')) // 4
console.log(fruits) // [ 'Kiwi', 'Banana', 'Apple', 'Mango' ]

// This method changes the length of the array.
// To add items at the beginning of an array, use the unshift() method.
