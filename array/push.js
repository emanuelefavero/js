// push(), adds new items to the END of an array, and returns the new length.
const fruits = ['Banana', 'Apple', 'Mango']

console.log(fruits.push('Kiwi')) // 4
console.log(fruits) // [ 'Banana', 'Apple', 'Mango', 'Kiwi' ]

