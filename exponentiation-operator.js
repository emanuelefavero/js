// pow (exponentiation operator)
// The exponentiation operator (**) raises the first operand to the power of the second operand.
const pow = (x, y) => x ** y

console.log(pow(2, 3)) // 2 * 1 = 2, 2 * 2 = 4, 4 * 2 = 8
console.log(pow(2, 4)) // 2 * 1 = 2, 2 * 2 = 4, 4 * 2 = 8, 8 * 2 = 16

console.log(2 ** 3) // 2 * 1 = 2, 2 * 2 = 4, 4 * 2 = 8
console.log(2 ** 4) // 2 * 1 = 2, 2 * 2 = 4, 4 * 2 = 8, 8 * 2 = 16
