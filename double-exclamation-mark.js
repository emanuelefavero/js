// The !! double exclamation mark operator casts a value to a boolean
// TIP: It is a shorthand for Boolean(value)
let value1 = 0 // falsy value
let value2 = 1 // truthy value
console.log(!!value1) // false
console.log(!!value2) // true

// TIP: It can also be used to transform any value to its corresponding boolean
console.log(!!'hello') // true
console.log(!!'') // false
